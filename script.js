function handleFile(file) {
    if (!file.type.startsWith('image/')) {
        alert('이미지 파일만 업로드 가능합니다.');
        return;
    }

    // Load file locally using FileReader (No Backend)
    const reader = new FileReader();
    reader.onload = function (e) {
        const imgElement = document.getElementById('preview-image');
        imgElement.src = e.target.result;

        // Wait for image to be fully loaded into the DOM element before AI validation
        imgElement.onload = async () => {
            // Bypass validation if MediaPipe failed to load (e.g. strict CORS, offline)
            if (isModelsLoaded === 'failedFallback') {
                document.getElementById('image-upload').value = '';
                startScanning();
                return;
            }

            if (!isModelsLoaded) {
                alert("AI 객체 분석 모델 셋업 중입니다. 1~2초 후 액션을 다시 시도해주세요.");
                document.getElementById('image-upload').value = ''; // Reset input so user can try same file again
                return;
            }

            // Show a tiny 'checking' indicator
            const uploadBtn = document.querySelector('.btn-primary');
            const originalText = uploadBtn.innerText;
            uploadBtn.innerText = "객체 판단 중...";
            uploadBtn.disabled = true;

            let isValid = true;
            let errorMessage = "";

            if (selectedType === '손금') {
                const handResult = handLandmarker.detect(imgElement);
                const faceResult = faceLandmarker.detect(imgElement);

                // Reject faces
                if (faceResult.faceLandmarks.length > 0) {
                    isValid = false;
                    errorMessage = "얼굴이 감지되었습니다! 손바닥 사진을 둥글게 펼쳐서 올려주세요.";
                } else {
                    // Check for a hand, with a strict confidence score to reject feet
                    let isDefiniteHand = false;
                    if (handResult.handednesses.length > 0) {
                        if (handResult.handednesses[0][0].score > 0.85) {
                            isDefiniteHand = true;
                        }
                    }

                    if (!isDefiniteHand) {
                        isValid = false;
                        errorMessage = "손이 정확히 감지되지 않았습니다. 손바닥 정면이 잘 보이는 사진인지, 혹은 발 사진은 아닌지 확인해주세요!";
                    }
                }
            } else if (selectedType === '관상') {
                const faceResult = faceLandmarker.detect(imgElement);
                if (faceResult.faceLandmarks.length === 0) {
                    isValid = false;
                    errorMessage = "얼굴이 감지되지 않았습니다. 관상을 볼 수 있게 얼굴 사진을 올려주세요!";
                }
            } else if (selectedType === '족금') {
                const handResultForFoot = handLandmarker.detect(imgElement);
                const faceResultForFoot = faceLandmarker.detect(imgElement);

                // MediaPipe Hand landmarker is aggressive and sometimes thinks toes/foot shapes are hands.
                // We add a strict confidence threshold (e.g. > 0.8) to only reject obvious hands.
                let isDefiniteHand = false;
                if (handResultForFoot.handednesses.length > 0) {
                    // handednesses[0][0].score contains the confidence score
                    if (handResultForFoot.handednesses[0][0].score > 0.85) {
                        isDefiniteHand = true;
                    }
                }

                if (isDefiniteHand) {
                    isValid = false;
                    errorMessage = "손이 강하게 감지되었습니다. 족금을 보시려면 발바닥(발) 사진을 올려주세요!";
                } else if (faceResultForFoot.faceLandmarks.length > 0) {
                    isValid = false;
                    errorMessage = "얼굴이 감지되었습니다. 족금을 보시려면 발바닥(발) 사진을 올려주세요!";
                }
            }

            uploadBtn.innerText = originalText;
            uploadBtn.disabled = false;

            if (!isValid) {
                alert("🚨 AI 분석 경고: " + errorMessage);
                document.getElementById('image-upload').value = ''; // Reset input
                return; // Stop execution, don't go to scanning view
            }

            // If valid, clear input and proceed to the fake scanner
            document.getElementById('image-upload').value = '';
            startScanning();
        };
    };
    reader.readAsDataURL(file);
}

function switchView(viewId) {
    document.querySelectorAll('.view').forEach(v => {
        if (v.id === viewId) {
            v.style.display = 'block'; // Ensure it's block before adding active
            // Small timeout to allow transition
            setTimeout(() => {
                v.classList.add('active');
                v.classList.remove('hidden');
            }, 10);
        } else {
            v.classList.remove('active');
            // Hide it completely after fade out
            setTimeout(() => {
                v.classList.add('hidden');
                v.style.display = 'none';
            }, 500);
        }
    });
}

