let FilesetResolver, HandLandmarker, FaceLandmarker;
let handLandmarker;
let faceLandmarker;
let isModelsLoaded = false;

async function initializeMediaPipe() {
    try {
        // Use dynamic import to safely load the module from CDN even in non-module scripts
        const visionModule = await import("https://cdn.jsdelivr.net/npm/@mediapipe/tasks-vision@0.10.3/vision_bundle.mjs");
        FilesetResolver = visionModule.FilesetResolver;
        HandLandmarker = visionModule.HandLandmarker;
        FaceLandmarker = visionModule.FaceLandmarker;

        const vision = await FilesetResolver.forVisionTasks(
            "https://cdn.jsdelivr.net/npm/@mediapipe/tasks-vision@0.10.3/wasm"
        );

        // Initialize Hand Detection
        handLandmarker = await HandLandmarker.createFromOptions(vision, {
            baseOptions: {
                modelAssetPath: `https://storage.googleapis.com/mediapipe-models/hand_landmarker/hand_landmarker/float16/1/hand_landmarker.task`,
                delegate: "GPU"
            },
            runningMode: "IMAGE",
            numHands: 1
        });

        // Initialize Face Detection
        faceLandmarker = await FaceLandmarker.createFromOptions(vision, {
            baseOptions: {
                modelAssetPath: `https://storage.googleapis.com/mediapipe-models/face_landmarker/face_landmarker/float16/1/face_landmarker.task`,
                delegate: "GPU"
            },
            runningMode: "IMAGE",
            numFaces: 1
        });

        isModelsLoaded = true;
        console.log("MediaPipe Models Loaded Successfully (0-Cost local AI ready)");
    } catch (e) {
        console.warn("Failed to load MediaPipe models. Falling back to no-validation mode.", e);
        isModelsLoaded = 'failedFallback';
    }
}

